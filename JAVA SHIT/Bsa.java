import java.util.ArrayList;
import java.util.Scanner;

public class Bsa{
    public static void main(String[] args) {

        Scanner input = new Scanner(System.in);
        
        ArrayList<String> bookTitle = new ArrayList<>();
        ArrayList<String> Author = new ArrayList<>();
        ArrayList<Integer> numberOfPages = new ArrayList<>();

        
        System.out.print("Enter bookTitle: ");
        bookTitle.add(input.nextLine());

        System.out.print("Enter Author: ");
        Author.add(input.nextLine());
        
        System.out.print("Enter numberOfPages: ");
        numberOfPages.add(input.nextInt());





}

}
